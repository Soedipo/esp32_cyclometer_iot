void interruptFunction() // interrupt service routine
{
  revolutions++;
  elapsedTime = millis() - revolutionStartTime;
  revolutionStartTime = millis();
}

void timeCount()
{
  if (startTime - endTime > 1000)
  {
    currentTimeCount++;
    endTime = millis();
  }
}

